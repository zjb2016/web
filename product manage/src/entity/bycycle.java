package entity;

public class bycycle {
	private String brand;
	private String type;
	private String price;
	private String chengse;
	private String id;
	

	public bycycle() 
	{
		
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String u) {
		this.brand = u;
	}

	public String getType() {
		return type;
	}

	public void setType(String p) {
		this.type = p;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String r) {
		this.price = r;
	}

	public String getChengse() {
		return chengse;
	}

	public void setChengse(String c) {
		this.chengse = c;
	}
	
	public String getId() {
		return id;
	}

	public void setId(String c) {
		this.id = c;
	}
}
